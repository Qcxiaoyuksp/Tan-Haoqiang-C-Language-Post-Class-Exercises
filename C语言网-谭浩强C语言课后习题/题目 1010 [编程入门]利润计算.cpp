#include <stdio.h>

int main() {
	long long i, sum = 0;
	scanf("%lld", &i);
	if (i <= 100000) sum = i * 0.1;
	else if (i > 100000 && i <= 200000) sum = 100000 * 0.1 + (i - 100000) * 0.075;
	else if (i > 200000 && i <= 400000) sum = 100000 * 0.1 + (200000 - 100000) * 0.075 + (i - 200000) * 0.05;
	else if (i > 400000 && i <= 600000) sum = 100000 * 0.1 + (200000 - 100000) * 0.075 + (400000 - 200000) * 0.05 + (i - 400000) * 0.03;
	else if (i > 600000 && i <= 1000000) sum = 100000 * 0.1 + (200000 - 100000) * 0.075 + (400000 - 200000) * 0.05 + (600000 - 400000) * 0.03 + (i - 600000) * 0.015;
	else sum = 100000 * 0.1 + (200000 - 100000) * 0.075 + (400000 - 200000) * 0.05 + (600000 - 400000) * 0.03 + (1000000 - 600000) * 0.015 + (i - 1000000) * 0.01;
	
	
	printf("%lld", sum);
	return 0;
} 
